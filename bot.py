def split_message(text: str, max_length: int = 3800) -> list[str]:
    if len(text) <= max_length:
        return [text]

    parts = []
    while len(text) > max_length:
        split_at = text.rfind("\n", 0, max_length)
        if split_at == -1:
            split_at = max_length
        parts.append(text[:split_at])
        text = text[split_at:].strip()

    if text:
        parts.append(text)

    return parts


async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Здравствуйте! Я AI-помощник по вашему БКП.\n\n"
        "Тема:\n"
        "«Разработка системы анализа русскоязычных текстов с выделением тематических групп».\n\n"
        "Задайте вопрос о цели, архитектуре, модулях, технологиях, базе данных, "
        "тестировании или защите проекта."
    )


async def answer_question(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user_question = update.message.text

    direct_answer = get_direct_answer(user_question)
    if direct_answer:
        for part in split_message(direct_answer):
            await update.message.reply_text(part)
        return

    knowledge_base = load_knowledge_base()

    try:
        response = chat(
            model=OLLAMA_MODEL,
            messages=[
                {
                    "role": "system",
                    "content": SYSTEM_PROMPT,
                },
                {
                    "role": "user",
                    "content": f"""
База знаний проекта:
{knowledge_base}

Вопрос пользователя:
{user_question}

Ответь строго по теме БКП. Если точной информации нет, скажи, что информации в базе знаний недостаточно.
""",
                },
            ],
            options={
                "temperature": 0.1,
                "num_predict": 500
            },
        )

        answer = response["message"]["content"].strip()

        if not answer or len(answer) < 5:
            answer = (
                "Информации недостаточно для корректного ответа. "
                "Нужно добавить этот вопрос и правильный ответ в knowledge_base.txt."
            )

    except Exception as e:
        answer = (
            "Произошла ошибка при обращении к Ollama.\n\n"
            "Проверьте:\n"
            "1. Запущен ли Ollama.\n"
            "2. Загружена ли модель.\n"
            "3. Совпадает ли название модели в .env.\n\n"
            f"Детали ошибки: {e}"
        )

    for part in split_message(answer):
        await update.message.reply_text(part)


def main():
    if not TELEGRAM_BOT_TOKEN:
        raise ValueError("TELEGRAM_BOT_TOKEN отсутствует в файле .env")

    app = Application.builder().token(TELEGRAM_BOT_TOKEN).build()

    app.add_handler(CommandHandler("start", start))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, answer_question))

    print(f"BKP Telegram bot is running with Ollama model: {OLLAMA_MODEL}")
    app.run_polling()


if __name__ == "__main__":
    main()